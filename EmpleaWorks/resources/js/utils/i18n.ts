function escapeRegExp(string: string) {
    return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

export function useTranslation() {
    const { locale } = usePage<SharedData>().props;
    const currentLocale = locale?.current || 'es'; // Default a espa√±ol
    
    // Funci√≥n para traducir un texto
    const t = (key: string, params?: Record<string, any>): string => {
        let text = translations[currentLocale]?.[key] || key;
        
        // Si hay par√°metros, reemplazarlos en el texto
        if (params) {
            Object.entries(params).forEach(([paramKey, value]) => {
                text = text.replace(
                    new RegExp(escapeRegExp(`{${paramKey}}`), 'g'),
                    String(value)
                );
            });
        }
        
        return text;
    };
    
    return { t, currentLocale };
}